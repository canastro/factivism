<p>
  "Progressophobia," Pinker points out, is fed by an
  <a href="https://en.wikipedia.org/wiki/Availability_heuristic">
    "availability heuristic"
  </a>
  through which people estimate the probability of a disaster occurring by
  extrapolating from vivid stories they have seen on the news. Thus, they obsess
  about flying, even though airplane crashes are extremely rare; about crime,
  even though crime rates have fallen; and about ISIS terrorists. Our
  "negativity bias," Pinker adds, also provides a market for purveyors of bad
  news.
</p>

